  `description`       text,
  `record_date`       timestamp NOT NULL DEFAULT CURRENT_TIMESTAMP,
  PRIMARY KEY (`id`),
  KEY FK_BILLING_BILLING_PERSON1_IDX_0 (`billing_person_id`),
  KEY FK_BILLING_USER1_IDX_0 (`user_id`)
